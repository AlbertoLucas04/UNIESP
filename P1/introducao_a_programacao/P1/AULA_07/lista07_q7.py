num = int(input('Insira um número para descobrir o seu fatorial: '))
fat = 1

if num< 0:
    print('Insira um valor positivo!')

elif num == 0:
  print('0! = 1')

else:
  
  for m in range(1, num+1):
    fat *= m

print(f'{num}! = {fat}')
    
    



    



































    
    
    
    
    
        
    

    
    
    
    
   
    
    
    




    