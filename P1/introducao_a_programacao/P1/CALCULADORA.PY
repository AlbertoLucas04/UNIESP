num_a = int(input('Digite um número: '))
num_b = int(input('Digite outro número: '))

soma = num_a + num_b
sub = num_a - num_b
div = num_a / num_b
mult = num_a * num_b

print(f'Resultado da soma: {soma}')
print(f'Resultado da subtração: {sub}')
print(f'Resultado da Divisão: {div}')
print(f'Resultado da Multipplicação: {mult}')